package classesAuxiliaresServicos;

public class Categoria {

	private int id;
	private double codigoSistema;
	private String nome;

	public int getId() {

		return id;
	}

	public void setId(int id) {

		this.id = id;
	}

	public double getCodigoSistema() {

		return codigoSistema;
	}

	public void setCodigoSistema(double codigoSistema) {

		this.codigoSistema = codigoSistema;
	}

	public String getNome() {

		return nome;
	}

	public void setNome(String nome) {

		this.nome = nome;
	}
}
